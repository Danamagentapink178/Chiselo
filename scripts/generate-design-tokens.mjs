#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const tokensPath = join(root, "design-tokens.json");
const swiftPath = join(root, "Chiselo", "MaterialTheme.swift");
const cssPath = join(root, "Chiselo", "Resources", "Editor", "design-tokens.css");

const tokens = JSON.parse(readFileSync(tokensPath, "utf8"));

function hexToRGB(hex) {
  const value = hex.replace("#", "");
  if (!/^[0-9a-f]{6}$/i.test(value)) {
    throw new Error(`Expected 6-digit hex color, got ${hex}`);
  }
  return {
    r: parseInt(value.slice(0, 2), 16),
    g: parseInt(value.slice(2, 4), 16),
    b: parseInt(value.slice(4, 6), 16)
  };
}

function number(value) {
  return Number(value).toFixed(3).replace(/0+$/, "").replace(/\.$/, "");
}

function cssColor(color) {
  const { r, g, b } = hexToRGB(color.hex);
  const alpha = color.alpha ?? 1;
  if (alpha >= 1) return color.hex.toUpperCase();
  return `rgba(${r}, ${g}, ${b}, ${number(alpha)})`;
}

function swiftColor(color) {
  const { r, g, b } = hexToRGB(color.hex);
  const alpha = color.alpha ?? 1;
  const parts = [
    `red: ${number(r / 255)}`,
    `green: ${number(g / 255)}`,
    `blue: ${number(b / 255)}`
  ];
  if (alpha < 1) parts.push(`opacity: ${number(alpha)}`);
  return `Color(${parts.join(", ")})`;
}

function swiftName(name) {
  if (name === "small") return "radiusSmall";
  if (name === "medium") return "radiusMedium";
  if (name === "panel") return "radiusPanel";
  if (name === "stage") return "radiusStage";
  return name;
}

const swiftLines = [
  "// Generated by scripts/generate-design-tokens.mjs from design-tokens.json.",
  "// Do not edit by hand.",
  "",
  "import SwiftUI",
  "",
  "enum MaterialTheme {"
];

for (const [name, color] of Object.entries(tokens.colors)) {
  swiftLines.push(`    static let ${name} = ${swiftColor(color)}`);
}

for (const [name, value] of Object.entries(tokens.radii)) {
  swiftLines.push(`    static let ${swiftName(name)}: CGFloat = ${value}`);
}

for (const [name, value] of Object.entries(tokens.spacing)) {
  swiftLines.push(`    static let ${name}: CGFloat = ${value}`);
}
swiftLines.push("}");
swiftLines.push("");

const cssLines = [
  "/* Generated by scripts/generate-design-tokens.mjs from design-tokens.json. */",
  "/* Do not edit by hand. */",
  "",
  ":root {",
  "  color-scheme: light;"
];

for (const color of Object.values(tokens.colors)) {
  cssLines.push(`  --${color.css}: ${cssColor(color)};`);
}
cssLines.push(`  --guide: ${cssColor(tokens.colors.primary)};`);
cssLines.push(`  --shadow: ${tokens.shadows.editorCanvas};`);
cssLines.push(`  --radius-small: ${tokens.radii.small}px;`);
cssLines.push(`  --radius-medium: ${tokens.radii.medium}px;`);
cssLines.push(`  --radius-panel: ${tokens.radii.panel}px;`);
cssLines.push(`  --radius-stage: ${tokens.radii.stage}px;`);
for (const [name, value] of Object.entries(tokens.spacing)) {
  const cssName = name.replace(/[A-Z]/g, (match) => `-${match.toLowerCase()}`);
  cssLines.push(`  --${cssName}: ${value}px;`);
}
cssLines.push(`  font-family: ${tokens.fontFamily};`);
cssLines.push("}");
cssLines.push("");

writeFileSync(swiftPath, swiftLines.join("\n"));
writeFileSync(cssPath, cssLines.join("\n"));

console.log(`Generated ${swiftPath}`);
console.log(`Generated ${cssPath}`);
